namespace EnrichmentProgressBundle\Data;

use Pimcore\Model\DataObject\ClassDefinition\Data\CalculatedValue;

class EnrichmentProgress extends CalculatedValue
{
    /**
     * @var string
     */
    public $queryColumnType = 'tinyint';

    public function __construct()
    {
        parent::setCalculatorClass(EnrichmentProgressCalculator::class);
    }

    public function getFieldType(): string
    {
        return 'enrichmentProgress';
    }

    /**
     * @return string
     */
    public function getQueryColumnType(): string
    {
        return $this->queryColumnType;
    }

    public function setCalculatorClass($calculatorClass): void
    {
    }

    /**
     * @param mixed $data
     * @param mixed $object
     * @param array $params
     * @return mixed
     */
    public function getDataForGrid($data, $object = null, $params = []): mixed
    {
        if (is_numeric($data)) {
            return $data / 100;
        }

        return null;
    }

    public function getDataForEditmode($data, $object = null, $params = []): string
    {
        return parent::getDataForEditmode($data, $object, $params) . "%"; // TODO: Change the autogenerated stub
    }
}
